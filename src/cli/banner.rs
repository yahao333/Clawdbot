//! 启动横幅模块
//!
//! 在程序启动时显示 ASCII 艺术横幅和版本信息
///
/// # 输出示例
/// ```
/// ╔══════════════════════════════════════════════════╗
/// ║           Rust Clawdbot v0.1.0                  ║
/// ║      高性能 AI 消息机器人 - Rust 实现            ║
/// ╚══════════════════════════════════════════════════╝
/// ```

/// 显示启动横幅
///
/// # 日志记录
/// - INFO: 显示横幅内容
pub fn show() {
    println!();
    println!(r"╔══════════════════════════════════════════════════════════╗");
    println!(r"║           Rust Clawdbot v0.1.0                           ║");
    println!(r"║      高性能跨平台 AI 消息机器人 - Rust 实现              ║");
    println!(r"╚══════════════════════════════════════════════════════════╝");
    println!();

    tracing::info!("程序启动横幅已显示");
}

/// 获取版本字符串
///
/// # 返回值
/// 版本号字符串，如 "0.1.0"
pub fn version() -> &'static str {
    env!("CARGO_PKG_VERSION")
}

/// 获取程序名称
///
/// # 返回值
/// 程序名称字符串，如 "clawdbot"
pub fn name() -> &'static str {
    env!("CARGO_PKG_NAME")
}
